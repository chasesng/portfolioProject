<script setup>
import { ref } from 'vue'
import { getFirestore, addDoc, collection } from '@firebase/firestore';
import { app } from '@/configs'
const db = getFirestore(app);

const name = ref('')
const email = ref('')
const enquiry = ref('')
const selectCategory = ref('')

var errMsg = ref('');

function checkComplete(input) {
  if (String(input).trim() != '') {
    return true
  }
  else {
    return false
  }
}


function checkEmail(input) {
  const regex = /^[\w-.]+@([\w-]+\.)+[\w-]{2,4}$/;
  if (!regex.test(input) === true) {
    return false;
  }
  else {
    return true;

  }

}

const checkInput = () => {
  const catPass = checkComplete(selectCategory.value)
  const usernamePass = checkComplete((name.value));
  const emailPass = checkEmail((email.value));
  const enquirePass = checkComplete(enquiry.value)

  return catPass + usernamePass + emailPass + enquirePass
}

const submitEnquiry = () => {
  if (checkInput() === 4) {
    addDoc(collection(db, 'enquiries'), {
      email: email.value.trim(),
      enquiry: enquiry.value.trim(),
      selectCategory: selectCategory.value,
      name: name.value.trim(),
      dateSubmit: Date.now(),
    })
    errMsg.value = "Successfully sent in your enquiry!";
    email.value = "";
    enquiry.value = "";
    selectCategory.value = "";
    name.value = "";
    this.closeContactPane()
    this.closeContactPaneMobile()



  }
  else {

    errMsg.value = "One or more fields are incomplete. Please try again."

  }

}
</script>



